// Initialize addThis to 0.
let addThis = 0;
// Initialize sum to 0.
let sum = 0;

// Use while loop to increment sum by addThis until sum is 10 (inclusive).
while (addThis < 10) {
  // Increment sum by addThis.
  sum += addThis;
  // Increment addThis by 1.
  addThis++;
}

// Console log sum.
console.log(sum);