let myArray = 10;

// countDown starting at value: 10
// and decrementing by 1 until it reaches 0. We need ten decrementing steps to reach 0.
// initialize a for loop with a counter variable i = 1. Only iterate if i <= 10.
for (let i = 1; i <= 10; i++) {
// decrement i by 1 each time the loop runs.
  myArray--;
}

console.log(myArray); // 0  
